# Integers
print(2 + 4)
print(2 - 4)
print(2 * 4)
print(2 / 4)

print(type(2 + 4))
print(type(2 / 4)) # float

print(2 ** 2) # Two to the power of 2

print(2 // 4) # 0 -> Integer division

print(5 // 4) # 1

print(5 % 4) # 1 - Remainder of the integer division

# math functions
print(round(3.1)) # 3
print(round(3.9)) # 4

print(abs(-5)) # 5
